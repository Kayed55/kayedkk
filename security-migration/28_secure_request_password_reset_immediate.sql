-- ============================================================================
-- م-أمان (26): إغلاق فوري لثغرة request_password_reset (سرقة حساب عبر المسار الذاتي المجهول)
-- ----------------------------------------------------------------------------
-- المشكلة: النسخة الحالية (ملف 23) عامة بلا جلسة، تُعيد كلمة المرور المؤقتة في الاستجابة،
--          فأي مجهول يعرف بريد موظف يعيد تعيين كلمته ويستلمها → سيطرة كاملة.
-- الإغلاق (أ): لا تُغيّر أي كلمة مرور، لا تُعيد أي كلمة مرور، ترجع رسالة عامة موحّدة
--             (تمنع كشف وجود الحسابات/التعداد). إعادة التعيين تتم عبر admin_reset_password
--             (المؤمَّنة: جلسة أدمن + Audit Log) حتى توثيق نطاق Resend ثم تفعيل المسار (ب).
-- آمن: توقيع الدالة كما هو (لا drop لكائنات معتمدة)؛ فقط منطق الجسم يتغيّر.
-- ============================================================================

create or replace function public.request_password_reset(p_email text)
returns table(ok boolean, temp_password text, user_email text, user_name text, message text)
language plpgsql
security definer
set search_path to 'public'
as $function$
begin
  -- رسالة عامة دائمًا بصرف النظر عن وجود البريد (تمنع التعداد)،
  -- وبلا أي تعديل على كلمة المرور (تتم إعادة التعيين عبر مدير النظام).
  return query select
    true,
    null::text,   -- لا تُعاد كلمة المرور إطلاقًا
    null::text,
    null::text,
    'إذا كان البريد مسجّلًا فستصلك كلمة مرور مؤقتة. لإعادة التعيين فورًا تواصل مع مدير النظام.'::text;
end;
$function$;

-- ملاحظات:
-- • admin_reset_password (ملف 23) تبقى كما هي: أدمن فقط + تسجيل تدقيق + تُعرض المؤقتة مرّة للأدمن.
-- • لا حاجة لتغيير صلاحية anon على هذه الدالة — أصبحت غير ضارّة (لا تكتب ولا تُسرّب).
-- • المسار الهدف (ب) — Rate limiting + إرسال عبر Resend بلا إرجاع — يُطبَّق لاحقًا بعد توثيق النطاق.
